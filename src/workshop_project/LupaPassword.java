    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        try{
            String sql = "SELECT * FROM pegawai WHERE nik = '"+ txt_nik.getText()+"'";
            Connection conn = (Connection)Config.configDB();
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            if(rs.next()){
                if(txt_nik.getText().equals(rs.getString("nik"))){
                    String sql1 = "update pegawai set password ='"+ txt_psd.getText() 
                            +"' where nik = '"+txt_nik.getText()+"'";
                    Statement statement = (Statement) conn.createStatement();
                    statement.executeUpdate(sql1);
                    statement.close();
                    JOptionPane.showMessageDialog(null, "berhasil submit");
                    new Login().setVisible(true);
                    this.dispose();
                }
            }else{
                JOptionPane.showMessageDialog(null,"NIK anda salah");
        }
        }catch (Exception e){
                     JOptionPane.showMessageDialog(this, e.getMessage());
            }
        
    }//GEN-LAST:event_btn_submitActionPerformed

