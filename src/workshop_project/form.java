    private void tampildata() {
        try
        {
            Object[] judul_kolom =
            {
                "Id Pendapatan", "Keterangan Pendapatan", "Jumlah Pendapatan", "Tanggal Pendapatan"
            };
            DefaultTableModel tabModel = new DefaultTableModel(null, judul_kolom);
            Table1.setModel(tabModel);

            java.sql.Connection conn = (Connection) Config.foderoDB();
            java.sql.Statement stm = conn.createStatement();
            tabModel.getDataVector().removeAllElements();

            java.sql.ResultSet rs = stm.executeQuery("SELECT * from pendapatan ");
            while (rs.next())
            {
                Object[] data =
                {
                    //rs.getString("No."),
                    rs.getString("Id Pendapatan"),
                    rs.getString("Keterangan Pendapatan"),
                    rs.getString("Jumlah Pendapatan"),
                    rs.getString("Tanggal Pendapatan"),
                };
                tabModel.addRow(data);
            }
        } catch (Exception ex)
        {
            System.err.println(ex.getMessage());
        }
    }

