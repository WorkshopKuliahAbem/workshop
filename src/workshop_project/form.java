    private void Table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table1MouseClicked
        // TODO add your handling code here:
        int baris = Table1.rowAtPoint(evt.getPoint());
        String keterangan = Table1.getValueAt(baris, 2).toString();
        ket.setText(keterangan);
        String jumlah = Table1.getValueAt(baris, 3).toString();
        txtjumlah.setText(jumlah);
//       String date = Table1.getValueAt(baris, 4).toString();
        Date date = null;
        try
        {
            date = new SimpleDateFormat("yyyy-MM-dd").parse(Table1.getValueAt(baris, 4).toString());
        } catch (ParseException ex)
        {
            Logger.getLogger(formpendapatan.class.getName()).log(Level.SEVERE, null, ex);
        }
        plhtanggal.setDate(date);
        id = Integer.valueOf(Table1.getValueAt(baris, 1).toString());
    }//GEN-LAST:event_Table1MouseClicked

