    private void table() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("No.");
        tbl.addColumn("Id Pendapatan");
        tbl.addColumn(("Keterangan Pendapatan"));
        tbl.addColumn("Jumlah Pendapatan");
        tbl.addColumn("Tanggal Pendapatan");
        try
        {
            int no = 1;
            String sql = "SELECT * FROM pendapatan";
            java.sql.Connection conn = (Connection) Config.foderoDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet rs = stm.executeQuery(sql);
            while (rs.next())
            {
                tbl.addRow(new Object[]
                {
                    no++,
                    rs.getString("id_pendapatan"),
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3)
                });
            }
            Table1.setModel(tbl);

        } catch (SQLException e)
        {

        }
    }

